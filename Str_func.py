my_string = "hello world"

# Convert the string to uppercase
upper_case = my_string.upper()
print("Uppercase:", upper_case)

# Convert the string to lowercase
lower_case = my_string.lower()
print("Lowercase:", lower_case)

# Capitalize the string (first character uppercased, rest lowercased)
capitalized = my_string.capitalize()
print("Capitalized:", capitalized)

# Convert the string to title case (each word capitalized)
title_case = my_string.title()
print("Title Case:", title_case)

# Swap the case of the string (upper to lower and vice versa)
swap_case = my_string.swapcase()
print("Swap Case:", swap_case)
